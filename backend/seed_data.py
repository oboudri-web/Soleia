def orientation_label(degrees: float) -> str:
    """Convertit degrés en label texte français."""
    if degrees is None:
        return "inconnue"
    d = degrees % 360
    if d < 22.5 or d >= 337.5:
        return "Nord"
    elif d < 67.5:
        return "Nord-Est"
    elif d < 112.5:
        return "Est"
    elif d < 157.5:
        return "Sud-Est"
    elif d < 202.5:
        return "Sud"
    elif d < 247.5:
        return "Sud-Ouest"
    elif d < 292.5:
        return "Ouest"
    else:
        return "Nord-Ouest"


